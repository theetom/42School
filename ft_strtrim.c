#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int	c;
	int d;

	c = 0;
	if (s1[0] == set[0])
		while (s1[c] == set[c])
			c++;
	if (set[c] == '\0')
		d++;
	c = ft_strlen(s1) - 1;
	if (s1[c] == set[0])
		while (s1[c] == set[c])
			c++;
	if (set[c] == '\0')
		d++;
}